    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int existe=0;
        gestor.eliminarCuenta(jTextField1.getText());
        for(Object object1:cuad.getCuentas()){               
            Cuadrilla aux1 =new Cuadrilla();
            aux1=(Cuadrilla)object1;
            if(aux1.getMedico().getDni().equals(jTextField1.getText())){
                cuad.eliminarCuenta(aux1.getIdcuadrilla());
                existe=1;
                jTextField2.setText(aux1.getMedico().getNombre());
                
                
            }
            break;
        }
        if (existe==1){
            JOptionPane.showMessageDialog(rootPane, "Medico encontrado");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Medico no encontrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

